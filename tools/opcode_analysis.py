    return {
        0x8000: "HandshakeB (C→S hello)",
        0x8001: "HandshakeA (S→C hello)",
        0x8003: "HandshakeC (S→C ready)",
        0x8301: "AuthB (per-character auth)",
        0x8305: "UDPServerData",
        0x830c: "Location (zone change)",
        0x830d: "GameinfoReady",
        0x8317: "? (rare)",
        0x8381: "AuthAck",
        0x8383: "InfoServer reply / server-list entry",
        0x8385: "CharList",
        0x8386: "Char-create ack (NEW from CREATION_LEVELING_LONG)",
        0x838f: "Interaction-state marker",
        0x8480: "Auth (account)",
        0x8482: "GetCharList / CharCreate (discriminator-based)",
        0x8737: "GetGamedata",
        0x873a: "Gamedata",
        0x873c: "GetUDPConnection (no-op on retail)",
        0xa001: "SessionReady-S (server)",
        0xa002: "Session ack? (S→C, fires on interactions)",
        0xa003: "SessionReady-C (client)",
    }.get(op, "?")


