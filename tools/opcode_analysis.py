def _multipart_disc_name(disc):
    return {
        0x01: "CharInfo (login-time)",
        0x02: "CharsysInfo",
        0x03: "? (only seen in CREATION_LEVELING_LONG)",
        0x04: "? (only seen in CREATION_LEVELING_LONG)",
        0x38: "? (only seen in PLAZA_TO_PEPPER cross-district)",
    }.get(disc, "?")


def _predicted_map(raw, reliable):
    """Generate a predicted opcode map showing observed + gap rows."""
    lines = []
    lines.append("### Connection / low-level (`0x00` – `0x0f`)\n\n")
    lines.append("These are the smallest-numbered opcodes — typical "
                 "convention: the lowest ranges hold the most "
                 "fundamental connection-level packets, which is what "
                 "we observe.\n\n")
    lines.append("| Op | Observed | Hypothesis |\n|---|---|---|\n")
    for op in range(0x00, 0x10):
        observed = "✓" if (op in raw or op in reliable) else "·"
        hint = _udp_name(op)
        lines.append(f"| `0x{op:02x}` | {observed} | {hint} |\n")
    lines.append("\n")

    lines.append("### Entity & state stream (`0x10` – `0x1f`)\n\n")
    lines.append("| Op | Observed | Hypothesis |\n|---|---|---|\n")
    for op in range(0x10, 0x20):
        observed = "✓" if (op in raw or op in reliable) else "·"
        hint = _udp_name(op)
        lines.append(f"| `0x{op:02x}` | {observed} | {hint} |\n")
    lines.append("\n")

    lines.append("### World events (`0x20` – `0x2f`)\n\n")
    lines.append("| Op | Observed | Hypothesis |\n|---|---|---|\n")
    for op in range(0x20, 0x30):
        observed = "✓" if (op in raw or op in reliable) else "·"
        hint = _udp_name(op)
        lines.append(f"| `0x{op:02x}` | {observed} | {hint} |\n")
    lines.append("\n")

    lines.append("### Player-info / metadata (`0x30` – `0x3f`)\n\n")
    lines.append("| Op | Observed | Hypothesis |\n|---|---|---|\n")
    for op in range(0x30, 0x40):
        observed = "✓" if (op in raw or op in reliable) else "·"
        hint = _udp_name(op)
        lines.append(f"| `0x{op:02x}` | {observed} | {hint} |\n")
    lines.append("\n")

    lines.append("### Sparse upper range (`0x40` – `0xff`)\n\n")
    lines.append("Only sporadic opcodes observed. Most are "
                 "single-occurrence echoes from the cipher seed in "
                 "the very first UDP packet of the session (where "
                 "the LFSR state hasn't stabilized yet) rather than "
                 "real protocol packets.\n\n")
    lines.append("| Op | Observed | Hypothesis |\n|---|---|---|\n")
    upper_observed = sorted(op for op in (set(raw) | set(reliable))
                            if op >= 0x40)
    for op in upper_observed:
        hint = _udp_name(op)
        lines.append(f"| `0x{op:02x}` | ✓ | {hint or '? — likely cipher echo'} |\n")
    lines.append("\n")

    return "".join(lines)


def main():
    data = load_data()
    captures = data.get("captures", [])
    packets = data.get("packets", {})
    out = Path("docs/protocol/OPCODE_STRUCTURE.md")
    write_report(packets, captures, out)
    print(f"wrote {out}")
    print(f"  {len(packets)} packet types analyzed")
    print(f"  {len(captures)} captures referenced")


if __name__ == "__main__":
    main()
